n=int(input())
print('*'*n, ' '*(n*2-3), '*'*n, sep='')
for i in range(n-2, 0, -1):
    print(' '*(n-1-i), '*', ' '*(n-2), '*', ' '*(i*2-1), '*', ' '*(n-2), '*', sep='')
print(' '*(n-1), '*', (' '*(n-2)+'*')*2, sep='')
for i in range(1, n-1):
    print(' '*(n-1-i), '*', ' '*(n-2), '*', ' '*(i*2-1), '*', ' '*(n-2), '*', sep='')
print('*'*n, ' '*(n*2-3), '*'*n, sep='')